using System;

class DeclaracaoVariaveis
{
	static void Main()
	{
		/*Declaracao de Variavel
		*/
		int a;
		
		//Inicialização da variavel
		a = 22;
		Console.WriteLine(a);
		
		//Retribuição de valor na variavel
		a = 56;
		Console.WriteLine(a);
		
		//Outras formas de Declaração de variaveis
		
		//Declarando e Inicializando a variavel
		int b = 925;
		Console.WriteLine(b);
		
		int y = 10, x = 20;
		int z = y + x;
		Console.WriteLine(z);
	}
}